CREATE TABLE IF NOT EXISTS STOCK(
    ID INT NOT NULL AUTO_INCREMENT,
    TICKER VARCHAR(255),
    STOCK_TYPE VARCHAR(255),
    EXCHANGE VARCHAR(255),
    PRICE DECIMAL,
    CURRENCY VARCHAR(255),
    VOLUME INT,
    STOCK_TIMESTAMP DATETIME,
    CONSTRAINT PK_STOCK PRIMARY KEY (ID)
);

